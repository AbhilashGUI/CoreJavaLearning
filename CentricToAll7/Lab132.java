package CentricToAll7;

public class Lab132
{
    public static void main(String[] args)
    {
        float f=sum(3.14f,6.28f,12.56f);
        System.out.println(f);
        float f1=sub(15.3f,2.3f,3.8f);
        System.out.println(f1);
    }



    public static float sum(float Vemula, float Abhilash, float Sharma)
            //sum represents the name of the function
    {
        return Vemula+Abhilash+Sharma;
    }
    public static float sub(float Abhilash, float Sharma, float Vemula)
    {
        return  Abhilash-Sharma-Vemula;
    }
}


