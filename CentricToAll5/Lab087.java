package CentricToAll5;

public class Lab087
{

        //While loop is used to iterate a block of code multiple times.
        //It is recommended to use, when we are not sure about the number of iterations
        //It is also known as entry controlled loop
        //In while loop increment/decrement happens at the end of the loop

        public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;   //If we don't provide the exit condition,it keeps printing
        }

    }
    }

