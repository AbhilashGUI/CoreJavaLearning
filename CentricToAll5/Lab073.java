package CentricToAll5;

public class Lab073
{
    public static void main(String[] args)
            //Printing the even numbers using for loop in descending/reverse order
    {
        for (int i=50; i>1; i--)
        {
            if(i % 5==0)
            {
                System.out.println("Even number is --> "+i);
            }
        }
    }
}
