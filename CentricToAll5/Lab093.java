package CentricToAll5;

public class Lab093
{
    public static void main(String[] args)
    {
        //factorial using while loop
        int i = 1, number = 7;
        int fact = 1;
        while (i <= number)
        {
            fact = fact * i;
            i++;
        }
            System.out.println("Factorial of " + number + " is " + fact);

        }
    }


