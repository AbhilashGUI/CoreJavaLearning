package CentricToAll2;

public class Lab015
{
    public static void main(String[] args)
    {
        //Arithmetic operators--->+,-,*,/,%
        byte b=70;
        int i=40;
        System.out.println(b+i);
        System.out.println("Sum of 2 numbers =" + (b+i));
        System.out.println(b-i);
        System.out.println("Sub of 2 numbers =" + (b-i));
        System.out.println(b*i);
        System.out.println("Mul of 2 numbers =" + (b*i));
        System.out.println(b/i);
        System.out.println("div of 2 numbers =" + (b/i));
        System.out.println(b%i);
        System.out.println("Mod of 2 numbers =" + (b%i));

    }
}
