package CentricToAll2;

public class Lab018
{
    public static void main(String[] args)
    {
        //Logical operators
        //Logical AND operators(&&)
        int i=10;
        int j=20;
        System.out.println(i<j&&j>i);  //Both the conditions should  be true

        //Logical OR operators
        int i1=30;
        int j1=40;
        System.out.println(i1>j1||i1<j1); //One of the condition should be true


        //Bitwise AND operators
         int i2=45;
         int j2=50;
         System.out.println(i2<j2&j2>i2);//Here both the conditions should be true/false , to get desired output

        //Bitwise OR operators
        int i3=60;
        int j3=80;
        System.out.println(i3>j3|j3>i3); //One conditions is true and o/p will be true



    }
}
