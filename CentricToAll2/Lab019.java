package CentricToAll2;

public class Lab019
{
    public static void main(String[] args)
    {
        //Assignment operators : = += -= *= /= %= &= ^= |= <<= >>= >>>=

        int i=15;
        int j=17;
        int k=19;
        int l=21;
        int m=23;
        i+=5;
        j-=4;
        k*=3;
        l/=7;
        m%=9;
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
        System.out.println(l);
        System.out.println(m);
    }
}
