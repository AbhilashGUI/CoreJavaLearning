package CentricToAll2;

public class Lab019
{
    public static void main(String[] args)
    {
        //Assignment operators : = += -= *= /= %= &= ^= |= <<= >>= >>>=

        int i=15;
        int j=17;
        int k=19;
        int l=21;
        int m=23;

        System.out.println(i+=15);
        System.out.println(j-=17);
        System.out.println(k*=19);
        System.out.println(l/=21);
        System.out.println(m%=23);  //It takes remainder as output
    }
}
